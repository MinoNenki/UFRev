export function normalizeLanguage(value?: string | null): Language {
  return value === 'pl' || value === 'es' || value === 'ru' || value === 'en' ? value : DEFAULT_LANGUAGE;
}

export function isPolish(language: Language) {
  return language === 'pl';
}

function fallbackTranslation(language: Language, key: string) {
  if (language === 'es') return ES_FALLBACKS[key];
  if (language === 'ru') return RU_FALLBACKS[key];
  return undefined;
}

export function tr<T extends string>(language: Language, values: TranslationValues<T>): T {
  if (language === 'pl') return values.pl;
  if (language === 'de') return (values.de ?? values.en) as T;
  if (language === 'es') return (values.es ?? fallbackTranslation('es', values.en) ?? values.en) as T;
  if (language === 'pt') return (values.pt ?? values.en) as T;
  if (language === 'ja') return (values.ja ?? values.en) as T;
  if (language === 'zh') return (values.zh ?? values.en) as T;
  if (language === 'id') return (values.id ?? values.en) as T;
  if (language === 'ru') return (values.ru ?? fallbackTranslation('ru', values.en) ?? values.en) as T;
  return values.en;
}
